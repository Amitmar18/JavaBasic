
public class TestFloat 
{
	public static void main(String[] args)
	{
		float a = 3.14f;
		System.out.println("Floating number a is: "+a);
		float b = (float) 6.18;
		System.out.println("floating number b is: " + b);
		double d = 12.36;
		System.out.println("floating number d is: " + d);
		double e = 12.36d;
		System.out.println("floating number e is: " + e);
		double f = (double) 12.36;
		System.out.println("floating number f is: " + f);
	}

}
